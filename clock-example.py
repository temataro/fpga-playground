# Converts a number to its binary representation in a list of strings
counter_to_list = lambda n: list(f"{n:0{N}b}")
indices = [4, 6, 9]


def highlight(indices, c):
    for i in c:
        if i in indices:
            c[i] = f"{ANSI_RST}{ANSI_BLU}{c[i]}{ANSI_RST}"

    return " ".join(c)


counter = 0
while True:
    counter += 1
    c = counter_to_list(counter)
    all_pass = True
    for idx in indices:
        if not int(c[idx]):
            all_pass = False

    if all_pass:
        print(f"\t{ANSI_GRN}{highlight(indices, c)}\n{ANSI_RST}{bongo_buff}", end="\r")
        sleep(CLOCK_TICK_MS / 1000)
        print(chr(27) + "[2J")
    else:
        print(f"\t{highlight(indices, c)}\n{bongo_smol}", end="\r")
